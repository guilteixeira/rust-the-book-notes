fn main() {
    // adição
    let soma: u8 = 5 + 10;
    
    // subtracao
    let diferenca = 95.5 - 4.3;

    // multiplicação
    let produto = 4 * 30;

    // divisao
    let quociente = 56.7 / 32.2;
    let truncado = -5 / 3;

    println!("{}", soma);
    println!("{diferenca}");
    println!("{produto}");
    println!("{quociente}");
    println!("{truncado}");
}
